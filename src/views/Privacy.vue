   },
   created(){
       window.addEventListener("scroll", this.scrollNav);
   },
   destroyed(){
       window.removeEventListener("scroll", this.scrollNav);
   },
   methods:{
       handleScroll() {
           const els = document.querySelectorAll
       },
       scrollNav() {
            let section = document.querySelectorAll('section');
            let navLinks = document.querySelectorAll('div nav a');
        
            section.forEach(sec =>{
               let top = window.scrollY;
               let offset = sec.offsetTop -250;
               let height = sec.offsetHeight;
               let id = sec.getAttribute('id');
    
               if(top >= offset && top < offset + height){
                   navLinks.forEach(links =>{
                       links.classList.remove('active');
                       document.querySelector('div nav a[href*=' + id +']').classList.add('active');
                   });
               }
           });
       },
   },
   
}
